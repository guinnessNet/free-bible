let totalPatched = 0;

for (const [translationId, versePatches] of Object.entries(patches)) {
  // Group by book
  const byBook = {};
  for (const p of versePatches) {
    if (!byBook[p.book]) byBook[p.book] = [];
    byBook[p.book].push(p);
  }

  for (const [bookId, bookPatches] of Object.entries(byBook)) {
    const filePath = path.join(BIBLES_DIR, translationId, `${bookId}.json`);
    const data = JSON.parse(fs.readFileSync(filePath, 'utf8'));

    for (const patch of bookPatches) {
      const chapter = data.chapters.find(c => c.chapter === patch.ch);
      if (!chapter) {
        console.error(`[ERROR] ${translationId} ${bookId} ${patch.ch}장을 찾을 수 없음`);
        continue;
      }

      const existingIdx = chapter.verses.findIndex(v => v.verse === patch.verse);
      if (existingIdx >= 0) {
        const existing = chapter.verses[existingIdx];
        if (existing.text && existing.text.trim() !== '') {
          console.log(`[SKIP] ${translationId} ${bookId} ${patch.ch}:${patch.verse} — 이미 텍스트 있음`);
          continue;
        }
        // 빈 텍스트인 경우 (EASY 합본 절) → 텍스트 업데이트
        chapter.verses[existingIdx].text = patch.text;
        console.log(`[UPDATE] ${translationId} ${bookId} ${patch.ch}:${patch.verse}`);
      } else {
        // 절 자체가 없는 경우 → 추가
        chapter.verses.push({ verse: patch.verse, text: patch.text });
        console.log(`[ADD] ${translationId} ${bookId} ${patch.ch}:${patch.verse}`);
      }

      // 절 번호 순으로 정렬
      chapter.verses.sort((a, b) => a.verse - b.verse);
      totalPatched++;
    }

    fs.writeFileSync(filePath, JSON.stringify(data, null, 2) + '\n', 'utf8');
  }
}

console.log(`\n총 ${totalPatched}절 패치 완료`);
